#include <unistd.h>

void	ft_is_negative(int n)
{
	int x;

	if (n < 0)
	{
		x = 'N';
		write(1, &x, 1);
	}
	else
	{
		x = 'P';
		write(1, &x, 1);
	}
	return ;
}
